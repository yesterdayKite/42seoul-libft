char	*ft_strchr(const char *s, int c)
{
	unsigned int cnt;

	cnt = -1;
	while (s[++cnt])
	{
		if (s[cnt] == c)
			return ((char*)s);
		++s;
	}
	if (c == '\0')
		return ((char*)s);
	return (0);
}
